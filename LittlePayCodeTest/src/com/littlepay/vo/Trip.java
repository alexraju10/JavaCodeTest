package com.littlepay.vo;

public class Trip {
	
	private String tripID;
	private String travelCardID;
	private String tapOnStop;
	private String tapOffStop;
	
	public String getTripID() {
		return tripID;
	}
	public void setTripID(String tripID) {
		this.tripID = tripID;
	}
	public String getTravelCardID() {
		return travelCardID;
	}
	public void setTravelCardID(String travelCardID) {
		this.travelCardID = travelCardID;
	}
	public String getTapOnStop() {
		return tapOnStop;
	}
	public void setTapOnStop(String tapOnStop) {
		this.tapOnStop = tapOnStop;
	}
	public String getTapOffStop() {
		return tapOffStop;
	}
	public void setTapOffStop(String tapOffStop) {
		this.tapOffStop = tapOffStop;
	}
	
	
	

}
